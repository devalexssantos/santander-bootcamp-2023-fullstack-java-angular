public class ForEmArrays {
    public static void main(String[] args) {
        
        String alunos [] = {"Pedro", "Tiago", "João", "André", "Simão", "Felipe"};

        for(int i = 0; i <= alunos.length; i++) {
            System.out.println("Indice: " + i + " | Aluno: " + alunos[i] + ".");
        }

    }
}
