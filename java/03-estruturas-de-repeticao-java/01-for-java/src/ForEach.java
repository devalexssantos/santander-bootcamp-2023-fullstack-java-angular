public class ForEach {

    public static void main(String[] args) {
        String apostolos [] = {"Pedro", "Tiago", "João", "André", "Simão", "Bartolomeu", "Felipe", "Tomé", "Mateus"}; 
   
       for(String apostolo : apostolos) {
            System.out.println("Apostolo: " + apostolo);
       }
   
    }

}
