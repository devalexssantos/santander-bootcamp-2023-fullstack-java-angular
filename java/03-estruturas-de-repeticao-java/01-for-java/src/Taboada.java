public class Taboada {
    public static void main(String[] args) {

        int num = 9;
        System.out.println("Taboada de: " + num);
        for(int i = 0; i <= 10; i++) {
            System.out.println(num + " x " + i + " = " + num * i);
        }

        System.out.println("Fim!");
    }
}
